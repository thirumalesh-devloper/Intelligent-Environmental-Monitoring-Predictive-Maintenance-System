	/* USER CODE BEGIN 2 */
	MPU6050_Init();
	sprintf(uart_buf_2, "Starting MPU6050 data reading...\r\n");
	HAL_UART_Transmit(&huart2, (uint8_t*) uart_buf_2, strlen(uart_buf_2),
	HAL_MAX_DELAY);
	static uint32_t valid_readings = 0;
	static uint32_t total_readings = 0;

	/* USER CODE END 2 */

